/**
 * @fn int suma(int, int)
 * @brief Funcion que suma 2 numeros enteros
 *
 * @param op1 Primer numero entero
 * @param op2 Segundo numero entero
 * @return Resultado de la suma de los numeros enteros
 */
int suma(int op1, int op2);

/**
 * @fn int resta(int, int)
 * @brief Funcion que resta 2 numeros enteros
 *
 * @param op1 Primer numero entero
 * @param op2 Segundo numero entero
 * @return Resultado de la resta del primer numero entero por el segundo
 */
int resta(int op1, int op2);

/**
 * @fn int multiplicacion(int, int)
 * @brief Funcion que multiplica 2 numeros enteros
 *
 * @param op1 Primer numero entero
 * @param op2 Segundo numero entero
 * @return Resultado de la multiplicacion de los numeros enteros
 */
int multiplicacion(int op1, int op2);

/**
 * @fn float division(int, int)
 * @brief Funcion que divide 2 numeros enteros
 *
 * @param op1 Primer numero entero
 * @param op2 Segundo numero entero
 * @return Resultado de la divison del primer numero entero por el segundo
 */
float division(int op1, int op2);

/**
 * @fn double factorial(int)
 * @brief Funcion que calcula el factorial de un numero
 *
 * @param op1 El numero
 * @return El resultado factorial del numero
 */
double factorial(int op1);
